def calc_max_lenth_of_tree_node(root: TreeNode):
    if not root.children:
        return 1
    else:
        _max: int = 0
        for child in root.children:
            _len = calc_max_lenth_of_tree_node(child)

            _max = _len if _len > _max else _max

        return 1 + _max


